<h3>CHI SIAMO</h3>
	
    <p>Il <strong>BiLUG</strong> (<strong>Biella Linux User Group</strong>) &egrave; un gruppo eterogeneo di persone
      che per lavoro o per passione utilizzano programmi liberi (Open
      Source) e si dedicano a promuoverne l'impiego in ogni settore
      applicativo, forti della convinzione che la libert&agrave; del software porti
      direttamente maggiore libert&agrave; agli utenti, nonch&eacute; applicazioni pi&ugrave;
      affidabili e funzionali.</p>
    <p>Nonostante il nome, i partecipanti del BiLUG non si dedicano solo a
      Linux ma si interessano all'intero spettro dei programmi liberi: tra
      di noi ci sono felici utenti BSD e molti usano programmi liberi su
      piattaforme Windows(r).</p>
    <p>Fulcro della nostra attivit&agrave; &egrave; la <strong>mailing list</strong> dove tutti i
      partecipanti possono ritrovarsi scambiandosi messaggi in email e dove
      i nuovi arrivati possono trovare l'aiuto di appassionati e
      professionisti competenti; il sito internet usato per la diffusione di
      news, approfondimenti e promozione di iniziative.</p>
    <p>Dal 2007 poi, grazie alla collaborazione con <strong>Cittadellarte -
      Fondazione Pistoletto</strong> che ci ospita, abbiamo la possibilit&agrave; di
      trovarci presso la <strong>sede</strong> in <strong>via Serralunga n.27 a Biella</strong> tutti i sabati
      pomeriggio, per accogliere i curiosi e gli interessati, per cercare di
      risolvere i pi&ugrave; comuni problemi in cui uno si pu&ograve; imbattere usando
      software libero, per dare consigli a chi ne abbia bisogno.<br />
      In particolar modo i membri del BiLUG offrono la loro consulenza ed
      esperienza nel settore per l'installazione e configurazione dei pi&ugrave;
      diffusi sistemi operativi (distribuzioni) ed applicativi liberi.</p>
    <p>Inoltre,verso l'inizio di ogni mese, in orario serale, il BiLUG
      organizza amichevoli <strong>riunioni</strong> per guidare i nuovi arrivati o per
      discutere le iniziative da intraprendere per sostenere la diffusione
      dei programmi liberi come, ad esempio, l'annuale manifestazione in
      occasione del Linux Day.</p>
    <h2>###</h2>
    <p><strong>BiLUG</strong> (<strong>Biella User Group</strong>) is a heterogeneous group of people that use
      open source applications either for business or passion and spend time to promote.It's use, we are strongly convinced that using open source software gives greater 
      freedom and stable applications to the users,best of all it's free and transparent.<br />
      <br />
      BiLUG members are not only interested in LINUX but all that affects the Open Source world, amongst us are also happy BSD and Windows users that prefer to use open source programs within their operating systems.</p>
    <p>Our primary means of communication once entered the BiLUG is done through the Mailing list, where all subscribers can meet each other and exchange email messages to find help and suggestions.<br />
      The web site www.bilug.it is used to share news and to promote activities.<br />
      <br />
      Since 2007 with the collaboration of Cittadellarte - Fondazione Pistoletto, we have weekly meetings, the whole of Saturday afternoon in via serralunga 27 in Biella.<br />
      Here we mainly discuss all the latest news and help fellow open source users with whatever open source problem they may encounter, this is an excellent way to learn from each other and at the same time meet people with a passionate look onto open source matters, this is truly a great opportunity to learn.<br />
      <br />
      And at the beginning of each month, BiLUG organizes a friendly reunion with all the new comers to help them orientate and to discuss what initiatives BiLUG has in plan to take through out the year like the LINUX DAY which is a yearly occurrence.</p>
    